package com.Glimmer.BuildType.BuildPattern;

/**
 * Product 抽象产品角色
 * @author Glimmer
 *
 */
public class Meal {

	public String food;
	public String drink;
	
	public String getFood() {
		return food;
	}
	public void setFood(String food) {
		this.food = food;
	}
	public String getDrink() {
		return drink;
	}
	public void setDrink(String drink) {
		this.drink = drink;
	}
}
